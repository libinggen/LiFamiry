import turtle
turtle.screensize(1500,1200)
l=turtle.Pen()
l.forward(500)
l.left(135)
l.forward(70.71)
l.left(45)
l.forward(400)
l.left(45)
l.forward(70.71)
l.backward(70.71)
l.left(45)
l.forward(50)
for x in range(4):
    l.penup()
    l.goto(50+100*(x+1), 50)
    l.pendown()
    l.forward(50)
l.right(90)
def wheel(x,y,r=25):
    l.penup()
    l.goto(x,y)
    l.pendown()
    l.circle(r)
wheel(50,0)
wheel(150,0)
wheel(350,0)
wheel(450,0)
def window(x,y,L=80,w=30):
    l.penup()
    l.goto(x,y)
    l.pendown()
    for X in range(2):
        l.left(90)
        l.forward(w)
        l.left(90)
        l.forward(L)
for i in range(4):
    window(60+100*i,40)
turtle.done()